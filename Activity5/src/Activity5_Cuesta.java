public class Activity5_Cuesta {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 5;
        int add = num1 + num2,
            sub = num1 - num2,
            mul = num1 * num2,
            div = num1 / num2;
        System.out.println(
                  "Number 1: " + num1 +
                "\nNumber 2: " + num2 +
                "\nResult of Addition: " + num1 + " + " + num2 + " = " + add +
                "\nResult of Subtraction: " + num1 + " - " + num2 + " = " + sub +
                "\nResult of Multiplication: " + num1 + " * " + num2 + " = " + mul +
                "\nResult of Division: " + num1 + " / " + num2 + " = " + div);
    }
}